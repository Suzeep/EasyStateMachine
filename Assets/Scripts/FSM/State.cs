using UnityEngine;
using System.Collections.Generic;

//=====================================================================================
// class State<T_OWNER, T_STATE>
//=====================================================================================
public class State<T_OWNER, T_STATE> : MonoBehaviour
{
	//--------------------------------------------------------------------------------
	// initialize
	//--------------------------------------------------------------------------------
	public virtual void		Enter(){}
	
	//--------------------------------------------------------------------------------
	// finalyze
	//--------------------------------------------------------------------------------
	public virtual void		Exit(){}
	
	//=====================================================================================
	// property
	//=====================================================================================
	public T_OWNER	Owner {
		get { return m_Owner; }
		set { m_Owner = value; }
	}
	public T_STATE	No {
		get { return m_No; }
		set { m_No = value; }
	}

	//=====================================================================================
	// member
	//=====================================================================================
	[SerializeField]
	private T_OWNER		m_Owner;
	[SerializeField]
	private T_STATE		m_No;
}